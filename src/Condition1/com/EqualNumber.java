package Condition1.com;

import java.util.*;


public class EqualNumber {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int a = sc.nextInt();
        int b = sc.nextInt();

        if (a == b)
            System.out.println("Equal");
        if (a > b) {
                System.out.println("a is great");
            } else {
                System.out.println("a is lesser");

            }
        }
    }

