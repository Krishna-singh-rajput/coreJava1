package SwitchVairiable;

import java.util.*;

public class Switch {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int button = sc.nextInt();
        switch(button) {
            case 1:
                System.out.println("krishna");
                break;
            case 2:
                System.out.println("atul");
                break;
            case 3:
                System.out.println("Himanshu");
                break;

        }
    }
}
